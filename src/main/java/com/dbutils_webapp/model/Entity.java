package com.dbutils_webapp.model;

public class Entity {
    Long id;
    String name;

    // Getter und Setter
    public Long getId() {
        return this.id;
    }
    
    public void setId(Long id) {
        this.id = id;
    }
    
    public String getName() {
        return this.name;
    }
    
    public void setName(String name) {
        this.name = name;
    }
}
